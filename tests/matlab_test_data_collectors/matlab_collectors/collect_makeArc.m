recorder = utils.TestRecorder('collectedValues/makeArc.mat');
for idx=1:length(all_params)
    disp(idx);

    params = all_params{idx};

    recorder.recordVariable('params', params);
    
    arc = makeArc(params{:});

    recorder.recordVariable('arc', arc);
    
    recorder.increment()
end

recorder.saveRecordsToDisk();